"""Thin async HTTPX wrapper around the Webex REST API.

Only the calls the inbound bridge needs:

* ``get_me()`` -- look up the bot's own personId so we can drop
  events generated by the bot itself (loop guard).
* ``get_message(message_id)`` -- fetch the actual message body. Webex
  webhooks deliver only an id; the body must be fetched explicitly.
* ``list_webhooks()`` / ``create_webhook()`` -- ensure exactly one
  ``messages.created`` webhook is registered on the bot's behalf.

Kept deliberately simple -- no retries, no caching, no plugins.
Errors propagate as ``httpx.HTTPStatusError`` so the caller decides
how to react (the dispatcher logs and drops the event; the lifespan
hook fails the startup so misconfiguration is loud).
"""

from __future__ import annotations

from typing import Any

import httpx


class WebexClient:
    """Bot-token-authenticated client for the Webex REST API."""

    def __init__(
        self,
        token: str,
        *,
        base_url: str,
        timeout: float = 15.0,
    ) -> None:
        # Authorization: Bearer <bot-token> on every call. We attach
        # it via default_headers so the call sites stay clean.
        self._client = httpx.AsyncClient(
            base_url=base_url.rstrip("/"),
            headers={"Authorization": f"Bearer {token}"},
            timeout=timeout,
        )

    async def aclose(self) -> None:
        await self._client.aclose()

    # ------------------------------------------------------------------
    # People
    # ------------------------------------------------------------------

    async def get_me(self) -> dict[str, Any]:
        """Return the authenticated bot's own ``person`` record.

        Used by the dispatcher to identify and drop events whose
        ``personId`` matches the bot itself, without which every
        ``post_message`` from a task would re-trigger the bridge in
        an infinite loop.
        """
        resp = await self._client.get("/people/me")
        resp.raise_for_status()
        return resp.json()

    # ------------------------------------------------------------------
    # Messages
    # ------------------------------------------------------------------

    async def get_message(self, message_id: str) -> dict[str, Any]:
        """Return the full Webex message record for ``message_id``.

        Webex webhook events only deliver the message id (intentionally:
        the webhook payload is sent over a customer-controlled URL and
        Webex doesn't want to leak conversation contents to a
        misconfigured endpoint). The bridge fetches the body server-
        side over an authenticated channel.
        """
        resp = await self._client.get(f"/messages/{message_id}")
        resp.raise_for_status()
        return resp.json()

    # ------------------------------------------------------------------
    # Webhook registration
    # ------------------------------------------------------------------

    async def list_webhooks(self) -> list[dict[str, Any]]:
        resp = await self._client.get("/webhooks")
        resp.raise_for_status()
        return resp.json().get("items", []) or []

    async def create_webhook(
        self,
        *,
        name: str,
        target_url: str,
        resource: str = "messages",
        event: str = "created",
        secret: str | None = None,
        filter_expression: str | None = None,
    ) -> dict[str, Any]:
        """Register a Webex webhook for ``resource``/``event`` on ``target_url``.

        ``secret`` enables Webex's HMAC-SHA1 signing of every delivery
        (header ``X-Spark-Signature``). Strongly recommended -- without
        it any client that knows the public URL can forge events.
        """
        body: dict[str, Any] = {
            "name": name,
            "targetUrl": target_url,
            "resource": resource,
            "event": event,
        }
        if secret is not None:
            body["secret"] = secret
        if filter_expression is not None:
            body["filter"] = filter_expression
        resp = await self._client.post("/webhooks", json=body)
        resp.raise_for_status()
        return resp.json()

    async def delete_webhook(self, webhook_id: str) -> None:
        resp = await self._client.delete(f"/webhooks/{webhook_id}")
        resp.raise_for_status()
