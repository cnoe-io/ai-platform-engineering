// Normalize agent name for lookup
export function normalizeAgentName(name: string): string {
  const normalized = name.toLowerCase().trim();

  // Handle common variations
  if (normalized.includes("argocd") || normalized === "argo") return "argocd";
  if (normalized === "aws" || normalized.includes("amazon")) return "aws";
  if (normalized === "github" || normalized.includes("git")) return "github";
  if (normalized === "jira" || normalized.includes("atlassian")) return "jira";
  if (normalized === "splunk") return "splunk";
  if (normalized === "pagerduty" || normalized === "pd") return "pagerduty";
  if (normalized === "confluence") return "confluence";
  if (normalized === "kubernetes" || normalized === "k8s") return "kubernetes";
  if (normalized === "caipe" || normalized.includes("platform")) return "caipe";
  if (normalized === "supervisor") return "supervisor";
  if (normalized === "rag" || normalized === "knowledge" || normalized.includes("knowledge")) return "rag";

  return normalized;
}

// Get agent logo config by name
export function getAgentLogo(agentName: string): AgentLogoConfig | null {
  const normalized = normalizeAgentName(agentName);
  return AGENT_LOGOS[normalized] || null;
}

// Agent Logo component
interface AgentLogoProps {
  agent: string;
  size?: "sm" | "md" | "lg";
  showFallback?: boolean;
  className?: string;
}

export function AgentLogo({ agent, size = "md", showFallback = true, className = "" }: AgentLogoProps) {
  const logo = getAgentLogo(agent);

  const sizeClasses = {
    sm: "w-4 h-4",
    md: "w-5 h-5",
    lg: "w-6 h-6",
  };

  if (!logo) {
    if (!showFallback) return null;

    // Fallback to first letter
    return (
      <div
        className={`${sizeClasses[size]} rounded-md bg-muted flex items-center justify-center text-xs font-bold ${className}`}
      >
        {agent.charAt(0).toUpperCase()}
      </div>
    );
  }

  return (
    <div
      className={`${sizeClasses[size]} rounded-md overflow-hidden bg-white/90 dark:bg-gray-800/90 p-0.5 ${className}`}
      title={logo.displayName}
    >
      {logo.icon}
    </div>
  );
}
